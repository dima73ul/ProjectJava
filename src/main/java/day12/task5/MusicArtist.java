package day12.task5;

public class MusicArtist {
    String nameArtist;
    int ageArtist;

    public MusicArtist(String nameArtist, int ageArtist) {
        this.nameArtist = nameArtist;
        this.ageArtist = ageArtist;
    }

    public String getNameArtist() {
        return nameArtist;
    }

    public int getAge() {
        return ageArtist;
    }

}
